#include "libft.h"

char	*ft_strtrim(char const *s1, char const *set)
{
	size_t	start;
	size_t	end;
	char	*result;

	if (!s1 || !set)
		return (NULL);
	start = 0;
	while (s1[start] && ft_strchr(set, s1[start]))
		start++;
	end = ft_strlen(s1);
	while (end > start && ft_strchr(set, s1[end - 1]))
		end--;
	end = end - start;
	result = (char *)malloc (end + 1);
	if (!result)
		return (NULL);
	ft_strlcpy(result, s1 + start, end + 1);
	return (result);
}	
